-l $WD/data/LD.txt \
-z $WD/data/GWAS_Z.txt \
-l $WD/data/LD.txt \
-z $WD/data/eQTL_Z.txt \
-f 1 -c 1 \
-o $WD/results/ecaviar_results.out
to run it